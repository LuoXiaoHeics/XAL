    def rrhf_loss(self, scores, rw_scores):
        rank_loss = 0.0
        for i in range(scores.shape[0]):
            diff = scores[i].unsqueeze(0) - scores[i].unsqueeze(-1) # b * b
            # print(diff)
            rw_diff = rw_scores[i].unsqueeze(0) - rw_scores[i].unsqueeze(-1) # b * b
            # print(rw_diff)
            aval = torch.bitwise_and(rw_diff > 0, diff < 0)
            # print(aval)
            # print(-diff[aval].sum())
            rank_loss += -diff[aval].sum().mean()
        return rank_loss
    
    def sft_loss(self, logits,labels, rw_scores):
        max_idx = torch.argmax(rw_scores,dim=1).unsqueeze(1)
        select_logits = torch.gather(logits, 1, max_idx.unsqueeze(2).unsqueeze(3).expand(-1, -1, logits.shape[-2], logits.shape[-1])).squeeze(1)
        select_labels = torch.gather(labels, 1, max_idx.unsqueeze(2).expand(-1,-1, labels.shape[-1])).squeeze(1)
        i = max_idx[0][0]
        loss_fct = CrossEntropyLoss(ignore_index=-100)
        ls = loss_fct(select_logits.view(-1, select_logits.size(-1)), select_labels.view(-1))
        # ls = -torch.gather(logit_label,1,max_idx).mean().mean()
        return ls


    def all_sft_loss(self, logit_label):
        ls = -logit_label.mean().mean()
        return ls

